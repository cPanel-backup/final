					<h2>Stem Cell Donation</h2>
					<img src="<?php echo base_url();?>uploads/blog/Stem cell design 04.jpg" /> 
					<p>An allogeneic stem cell transplant needs a ‘donor’ to ‘donate’ his hematopoietic stem cells to
						the patient. Finding a fully matched sibling donor is becoming increasingly difficult due to
						decreasing family size – there is a 25% chance of one’s sibling to be a fully compatible HLA
						match. It is understood that 70% of patients who require a HSCT wouldn’t be able to find a
						matched donor in the family. Every year 40,000 patients die because of not finding a voluntary
						stem cell donor in India. This creates a huge demand-supply gap and a very big hurdle in use of
						HSCT as a treatment option. </p>
					<h4>Is there a solution for patients without a donor in the family?</h4>
					<p>Yes. This is through voluntary stem cell donation. This is co-ordinated through various NGOs,
						public registries, which maintain the donor details. Some of the registries which do such
						activities are DATRI, MDR-I (Marrow donor registry-India) etc. DATRI is the largest such
						registry in India with 4 lakh+ registered donors.</p>
					<h4>Who can register?</h4>
					<p>Any healthy person between 18-55 years of age can register as a donor. This registration can
						happen through ‘donor registration camps’ conducted by the registries. Alternatively, a willing
						donor can reach out to the registry and send his sample across after giving the needed consent.
					</p>
					<h4>How safe is ‘registering’ as a donor?</h4>
					<p>It is very safe and easy. A buccal swab (from inside of cheeks) is collected from the donor, a
						consent is taken and the sample is shipped to the central lab. This sample gets processed at the
						lab and the donor HLA is identified and recorded in the registry. This data is looked up while
						trying to identify a match for a patient requiring HSCT.</p>
					<h4>What happens when a donor is identified for a patient?</h4>
					<p>The donor gets notified and a confirmatory sample is sent to cross check the results. After this,
						a health check is done for the donor to establish a normal health status of the donor and to
						rule out any transmissible infections. The donor gets referred to a nearest collection centre
						where a physician checks him/her and reports. G-CSF injections which mobilize stem cells from
						bone marrow to peripheral blood would be given for 4-5 days. Donor might experience mild body
						aches with this. Stem cell collection is done on 5th day. This is like donation of blood or
						platelets though it takes a longer time (approx. 4 hours). The donor might experience some
						tingling sensation around lips or on finger tips which goes away with a dose of calcium. The
						donor can resume normal activities after a brief rest of around 4-5 hours.</p>
