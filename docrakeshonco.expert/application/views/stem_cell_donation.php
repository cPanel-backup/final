					<p>The domain of stem cell registry in India is nascent at best – the probability of finding a match
						through this registry is a dismal 0.0008 percent! Indian donors find very scarce representation
						in international registries. This gap can only be bridged by having robust, native stem cell
						registry with HLA data covering at least 1% of population. </p>
